create table if not exists public.blog_posts (
  id bigint generated by default as identity primary key,
  slug text not null unique,
  title text not null,
  subtitle text,
  excerpt text,
  content text not null,
  cover_image_url text,
  author_id uuid not null references auth.users(id) on delete cascade,
  author_name text,
  status text not null default 'draft'
    check (status in ('draft', 'published')),
  tags text[] not null default '{}',
  published_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists blog_posts_slug_idx
on public.blog_posts(slug);
create index if not exists blog_posts_status_idx
on public.blog_posts(status);
create index if not exists blog_posts_published_at_idx
on public.blog_posts(published_at desc);
create index if not exists blog_posts_author_idx
on public.blog_posts(author_id);

alter table public.blog_posts enable row level security;

drop policy if exists "Public can read published blog posts"
on public.blog_posts;
create policy "Public can read published blog posts"
on public.blog_posts
for select
using (status = 'published');

drop policy if exists "Authors can read own blog posts"
on public.blog_posts;
create policy "Authors can read own blog posts"
on public.blog_posts
for select
to authenticated
using (auth.uid() = author_id);

drop policy if exists "Authors can create blog posts"
on public.blog_posts;
create policy "Authors can create blog posts"
on public.blog_posts
for insert
to authenticated
with check (auth.uid() = author_id);

drop policy if exists "Authors can update own blog posts"
on public.blog_posts;
create policy "Authors can update own blog posts"
on public.blog_posts
for update
to authenticated
using (auth.uid() = author_id)
with check (auth.uid() = author_id);

drop policy if exists "Authors can delete own blog posts"
on public.blog_posts;
create policy "Authors can delete own blog posts"
on public.blog_posts
for delete
to authenticated
using (auth.uid() = author_id);
